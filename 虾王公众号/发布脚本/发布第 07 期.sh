## ⚠️ 注意事项

1. 备份重要文件 - edit 和 write 可能意外覆盖
2. 权限检查 - 确保 Agent 有读写权限
3. 路径安全 - 避免路径遍历攻击
4. 敏感信息 - 不要将密钥写入日志
5. 并发冲突 - 多个 Agent 同时修改同一文件可能冲突

## 🚀 进阶技巧

组合使用模式：读取 → 修改 → 验证 → 写入

