## 🎯 为什么文件操作如此重要？

没有文件能力的 Agent：
- 无法保存工作成果
- 无法读取配置和数据
- 每次对话都是失忆状态

有文件能力的 Agent：
- 可以持久化记忆
- 能够处理批量数据
- 自动维护配置和日志
- 与其他工具协作

## 🔧 OpenClaw 的文件操作工具

### 1. read - 读取文件
适合读取配置文件、日志内容、数据文件。

### 2. write - 写入文件
创建或覆盖文件，用于生成报告、保存配置。

### 3. edit - 精确编辑
替换文件中的特定文本，用于修改配置、更新文档。

### 4. exec - 执行命令
执行 shell 命令，用于版本控制、构建部署、系统管理。

